    {
        $customer = auth('customer')->user();
        $invoice = Invoice::whereHas('contact', function ($q) use ($customer) {
            $q->where('invoices.contact_id', $customer->id);
        })->with(['payments', 'inventory', 'contact'])->findOrFail($id);

        return $this->returnData($invoice);
    }

